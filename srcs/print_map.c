#include "../include/wolf.h"

void	print_map(char **grid)
{
	int	i;
	int	j;

	i = 0;
	while (grid[i])
	{
		j = 0;
		while (grid[i][j])
		{
			write(1, &grid[i][j], 1);
			j++;
		}
		write(1, "-\n", 2);
		i++;
	}
}
